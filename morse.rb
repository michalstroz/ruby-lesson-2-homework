#!/usr/bin/env ruby

def decode_morse (morse_code)
  morse_dict = {
      "a" => ".-","b" => "-...","c" => "-.-.","d" => "-..","e" => ".","f" => "..-.","g" => "--.","h" => "....","i" => "..","j" => ".---","k" => "-.-","l" => ".-..","m" => "--","n" => "-.","o" => "---","p" => ".--.","q" => "--.-","r" => ".-.","s" => "...","t" => "-","u" => "..-","v" => "...-","w" => ".--","x" => "-..-","y" => "-.--","z" => "--.."," " => " ","1" => ".----","2" => "..---","3" => "...--","4" => "....-","5" => ".....","6" => "-....","7" => "--...","8" => "---..","9" => "----.","0" => "-----"
  }

  morse_code.split("  ").map{ |x| x.split(" ").map{ |v| morse_dict.key(v) }.join }.join
end

puts decode_morse('.... . .-.. .-.. --- .-- --- .-. .-.. -..')
